public class CapacityModel {

    private int n2;
    private int n4;
    private int n8;
    
    public CapacityModel(int n2, int n4, int n8) {
        this.n2 = n2;
        this.n4 = n4;
        this.n8 = n8;
    }

    public int getN2() {
        return this.n2;
    }

    public int getN4() {
        return this.n4;
    }

    public int getN8() {
        return this.n8;
    }
}
